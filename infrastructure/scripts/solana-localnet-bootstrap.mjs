import fs from 'node:fs/promises';
import path from 'node:path';

import bs58 from 'bs58';
import { Connection, Keypair, LAMPORTS_PER_SOL, PublicKey } from '@solana/web3.js';
import { createMint, getOrCreateAssociatedTokenAccount, mintTo } from '@solana/spl-token';

const RPC_URL = process.env.SOLANA_LOCALNET_RPC_URL || 'http://127.0.0.1:8899';
const OUT_PATH =
  process.env.SOLANA_LOCALNET_BOOTSTRAP_OUT ||
  path.resolve(process.cwd(), 'infrastructure', 'seed-data', 'solana-localnet-faucet.env');
const AIRDROP_SOL = Number(process.env.SOLANA_LOCALNET_AIRDROP_SOL || '20');
const CONFIRM_TIMEOUT_MS = 30_000;

function nowIso() {
  return new Date().toISOString();
}

async function confirmSignature(connection, signature) {
  const strategy = await connection.getLatestBlockhash('confirmed');
  const started = Date.now();
  for (;;) {
    const statuses = await connection.getSignatureStatuses([signature], {
      searchTransactionHistory: true,
    });
    const entry = statuses.value[0];
    if (entry?.confirmationStatus === 'confirmed' || entry?.confirmationStatus === 'finalized') {
      return;
    }
    if (entry?.err) {
      throw new Error(`solana_signature_failed:${JSON.stringify(entry.err)}`);
    }
    if (Date.now() - started > CONFIRM_TIMEOUT_MS) {
      throw new Error('solana_signature_confirmation_timeout');
    }
    const currentHeight = await connection.getBlockHeight('confirmed');
    if (currentHeight > strategy.lastValidBlockHeight) {
      throw new Error('solana_signature_blockhash_expired');
    }
    await new Promise((resolve) => setTimeout(resolve, 500));
  }
}

async function ensureFunded(connection, signer) {
  const current = await connection.getBalance(signer.publicKey, 'confirmed');
  const required = AIRDROP_SOL * LAMPORTS_PER_SOL;
  if (current >= required) return current;
  const sig = await connection.requestAirdrop(signer.publicKey, required - current);
  await confirmSignature(connection, sig);
  return connection.getBalance(signer.publicKey, 'confirmed');
}

async function main() {
  const connection = new Connection(RPC_URL, 'confirmed');
  const signer = Keypair.generate();
  const balance = await ensureFunded(connection, signer);

  const wrappedMint = await createMint(connection, signer, signer.publicKey, null, 9);
  const stableMint = await createMint(connection, signer, signer.publicKey, null, 6);
  const wrappedAta = await getOrCreateAssociatedTokenAccount(connection, signer, wrappedMint, signer.publicKey);
  const stableAta = await getOrCreateAssociatedTokenAccount(connection, signer, stableMint, signer.publicKey);

  await mintTo(connection, signer, wrappedMint, wrappedAta.address, signer, BigInt(500_000_000_000));
  await mintTo(connection, signer, stableMint, stableAta.address, signer, BigInt(5_000_000_000_000));

  const envText = [
    '# generated by infrastructure/scripts/solana-localnet-bootstrap.mjs',
    `# generated_at=${nowIso()}`,
    `XCLAW_TESTNET_FAUCET_RPC_URL_SOLANA_LOCALNET=${RPC_URL}`,
    `XCLAW_SOLANA_FAUCET_SIGNER_SECRET_SOLANA_LOCALNET=${bs58.encode(signer.secretKey)}`,
    `XCLAW_SOLANA_FAUCET_WRAPPED_MINT_SOLANA_LOCALNET=${wrappedMint.toBase58()}`,
    `XCLAW_SOLANA_FAUCET_STABLE_MINT_SOLANA_LOCALNET=${stableMint.toBase58()}`,
    'XCLAW_SOLANA_FAUCET_DRIP_NATIVE_UNITS_SOLANA_LOCALNET=1000000000',
    'XCLAW_SOLANA_FAUCET_DRIP_WRAPPED_UNITS_SOLANA_LOCALNET=1000000000',
    'XCLAW_SOLANA_FAUCET_DRIP_STABLE_UNITS_SOLANA_LOCALNET=1000000',
    'XCLAW_SOLANA_LOCALNET_AIRDROP_FALLBACK_SOLANA_LOCALNET=true',
    '',
  ].join('\n');

  await fs.mkdir(path.dirname(OUT_PATH), { recursive: true });
  await fs.writeFile(OUT_PATH, envText, 'utf8');

  console.log(
    JSON.stringify(
      {
        ok: true,
        rpcUrl: RPC_URL,
        signer: signer.publicKey.toBase58(),
        signerBalanceLamports: balance,
        wrappedMint: wrappedMint.toBase58(),
        stableMint: stableMint.toBase58(),
        outputEnvFile: OUT_PATH,
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(
    JSON.stringify(
      {
        ok: false,
        message: error instanceof Error ? error.message : String(error),
      },
      null,
      2
    )
  );
  process.exit(1);
});
